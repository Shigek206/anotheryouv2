<h3>Before the Procedure</h3>
<p>If you love the eyebrow shape that you currently draw in, then you can come to your appointment with them drawn in. If you are unsure about shape and need some guidance, then we will work on shape together.  No two sides of the face are even, so we want to make sure the brows we draw and then tattoo will be even.</p>

<p>We will also make sure that the chosen shape is the most flattering for your face. Tattooing will not begin until you are satisfied with the shape.</p>

<p>There is no need to remove eyebrow hairs that are within the shape we want to achieve. Please do not shave off your eyebrows.</p>

<h3>During the Procedure</h3>
<p>The eyebrow procedure is completed using the hair stroke technique. The closer the strokes are together, the more filled in the eyebrow will look. The slower the strokes, the more they resembles hair. The faster the strokes, the more they give a soft background appearance. The end result will be determined before the procedure begins.</p>

<p>Tattooing will take approximately 45 minutes. After the procedure, your eyebrows will appear darker than how you would want them to look when they are healed.  Within a week, the dark layer will slough off, and the softer color which is your tattoo will be what remains.</p>

<h3>After the Procedure</h3>
<p>I will see you 4-6 weeks after the initial procedure to do any necessary touch- ups, such as
darkening, thickening, adding a stroke, etc. There is no charge for this appointment.</p>

<p>Depending on the depth of color chosen, permanent eyebrows will last between 3-5 years for
brunettes and 1 1/2-2 years for blondes. There will still be color; the eyebrows will just get
lighter over the years. Years down the road when it is time to come back for a touch-up, then
only a touch-up rate will apply.</p>

