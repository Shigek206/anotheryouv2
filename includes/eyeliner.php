<h1>Permanent Eyeliner</h1>
<h5>The eyeliner procedure is a great way to define your eyes. The eyeliner tattoo can be bold and noticeable; a fine, thin line; or somewhere in between. You can choose to have just the upper or lower eyeliner done or both done together.</h5>

