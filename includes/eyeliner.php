</ul>

<h3>Before the Procedure</h3>
<p>I will suggest that you come to your appointment with your eyeliner drawn in so I can get an
idea of what you are used to seeing around your eyes. Everyone&#39;s idea of thick and thin is
different, so having a visual is always nice.  I like to stick with more natural colors such
as browns to blacks, but I also have blue, green and grey pigments.  The color
choice will be yours, but I can also make suggestions.

<h3>During the Procedure</h3>
<p>First, the area around your eyes will benumbed using a topical anesthetic. You should only feel pressure and some agitation during the procedure. If you feel a poking sensation, then I can apply more anesthetic. After the numbing, the eyeliner will be drawn in again using a surgical marker, and you will have the final say before the procedure actually begins.</p>

<p>When working on the lower eyeliner, your eyes will be open, and I will direct you in regards
to where I want you to look.  I work in short segments to give you plenty of breaks. No
rushing. When we get to your upper eyeliner, your eyes will remain closed.</p>

<p>Because of the frequent stops, the procedure takes about 15 minutes per quadrant. So lower eyeliner takes 30 minutes, upper takes 30 minutes, and both upper and lower together will take approximately an hour.</p>

<h3>After the Procedure</h3>
<p>After the procedure, I will have you look in the mirror. At first the eyeliner will look too thick and too dark, like you are wearing liquid liner, because your eyes will be puffy. This puffiness will go awy within 1 1/2 - 2 days.</p>
<p>You will be able to drive home with after care instructions as well as ointment for healing. Cool compression on the eyes will help ease swelling. The darkness will slough off in 5 - 7 days, and the softer color which is your tattoo will b what remains.</p>
<p>I like to see clients 4 to 6 weeks after the procedure to make any necessary adjustments such as making the eyeliner darker or fuller. Because the eyeliner is usually a dark coor and very saturated, you won't have to come back for any color enhancement for 3 - 5 years. You will only be charged to touch-up rate at that time. 


