<h3>What Happens During the Procedure?</h3>
<p>Before the procedure begins, I will draw in your lip line and balance out any differences between the sides of your mouth. Once I have drawn in the shape, then I will numb your lips. You should not feel the procedure at all.</p>
<p>Depending on the size of your lips, the procedure itself could take anywhere between 45 minutes to an hour and 15 minutes</p>

<h3>What Happens After the Procedure?</h3>
<p>After the procedure, I will provide aftercare instructions for you. Your lips will be very swollen and very bright even if a natural color is chosen. The swelling will last approximately three days.</p>
<p>On the third day, the swelling will be down and all the excess skin will slough off so expect to have chapped-looking lips for about a day. You may not see all the color immediately following the sloughing off stage. Sometimes it takes a week or so for <u>all</u> the color to be present.</p>
<p>Although I like to see my clients 4 - 6 weeks after the procedure, lips usually heal very well and do not need further work. Years down the road the color will soften; the depth of color you choose at the beginning will determine how long your lips wil sustain the color.</p>

<h3>FAQs About Permanent Full Lip Makeup</h3>
<p><strong>Can I get permanent lip liner alone?</strong><br/>
I do not recommend doing lip liner alone as it generally gives the lips a clownish appearance.
However if you have a minor scar that has caused loss of color in the lip line itself, then that
area can be adjusted to blend in with the rest of the lip.</p>

<p><strong>Can I workout after the procedure?</strong><br />
I suggest not working out after the procedure for a few days. This includes hot yoga.</p>

<p><strong>I get periodic cold sores. Can I still have the procedure?</strong><br />
Yes. You can still have the procedure, but be sure to let me know if you have ever had a cold sore. We would want to get you on an anti-viral prescription which we would discuss prior to the appointment.</p>

