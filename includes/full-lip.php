<h1>Permanent Full Lip Makeup</h1>
<h5>Permament full lip makeup creates youthful, symmetrical lips. During this procedure, lips will be lined to give you fullness and definition before being filled with color.</h5>

<h5>The color you choose can be very natural or more dramatic depending on preference. I always suggest that you bring in some lip-stick colors that you like so I get an idea if you prefer warm or cool tones and what depth of color you are comfortable with.</h5>

<h2>Permanent Lip Makeup Procedures include:</h2>
